package debug;

public class Calcolatrice {

	public double addizione(double x, double y) {
		return x * y;
	}
	
	public double moltiplicazione(double x, double y) {
		return x * y;
	}
	
	public double sottrazione(double x, double y) {
		return x - y;
	}
	
	public double divisione(double x, double y) {
		return x / y;
	}
	
	
}
